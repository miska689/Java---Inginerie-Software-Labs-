package org.example.lab5;

public interface IGeometricForm {
  /**
   * Returns the area of the geometric form.
   *
   * @param a the side of the geometric form
   * @return the area of the geometric form
   */
  double getArea(int a);

  /**
   * Returns the perimeter of the geometric form.
   *
   * @param a the side of the geometric form
   * @return the perimeter of the geometric form
   */
  double getPerimeter(int a);
}
